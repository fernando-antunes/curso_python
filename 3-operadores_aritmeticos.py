import math

num1 = 10
num2 = 2

print(num1 + num2) #soma
print(num1 - num2) #subtração
print(num1 * num2) #multiplicação
print(num1 / num2) #divisão
print(num1 % num2) #resto da divisao
print(num1 ** num2) #elevado a 
print(num1 // num2) #divisao sem casas decimais

# operação complexa
print((3 + 5) * (7 + 3))

#valor absoluto
print(abs(-15))

#elevado
print(pow(3,3))

#maior numero
print(max(1,6,7,43,0))

#menor numero
print(min(1,6,7,43,0))

#arredondamento
print(round(8.8))

